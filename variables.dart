main() {
  //Declaring integer to variable
  var side = 10;
  var area = side * side;
  print("Area of square is ${area} square units");

  //Declaring string to variable

  String name = "Sabana";
  print("Good morning ${name}");

  //final
  final int digit = 25;
  print(digit);

  //Declaring constant value

  var radius = 5;
  const pi = 3.14;
  var a = pi * radius * radius;
  print("Area of circle is ${a} square units");
}
